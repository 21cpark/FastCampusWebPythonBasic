// 90 - A, 80 - B, C - 70, D - 60, F 
var score = 100;
if(score >= 90){
    console.log("점수는 A입니다.");
}else if(score >= 80){
    console.log("점수는 B입니다.");
}else if(score >= 70){
    console.log("점수는 C입니다.");
}else if(score >= 60){
    console.log("점수는 D입니다.");
}else{
    console.log("점수는 F입니다.");
}

